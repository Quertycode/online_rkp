#!/bin/bash
# Скрипт для полной очистки БД и изображений (Linux/Mac)

echo "============================================================"
echo "  ОЧИСТКА БАЗЫ ДАННЫХ"
echo "============================================================"
echo ""
echo "ВНИМАНИЕ! Будут удалены ВСЕ данные и изображения!"
echo ""
read -p "Вы уверены? (введите 'да' для подтверждения): " confirm

if [ "$confirm" != "да" ]; then
    echo "Отменено."
    exit 0
fi

echo ""
echo "Очистка данных..."
python3 clean_db.py --confirm

if [ $? -ne 0 ]; then
    echo "ОШИБКА при очистке!"
    exit 1
fi

echo ""
echo "============================================================"
echo "  ГОТОВО! База данных очищена"
echo "============================================================"
echo ""


